static float SampleChainYawDelta(float timeSec) {
  if (timeSec <= CHAIN_YAW_LUT[0][0]) return CHAIN_YAW_LUT[0][1];
  if (timeSec >= CHAIN_YAW_LUT[CHAIN_YAW_LUT_SIZE-1][0])
    return CHAIN_YAW_LUT[CHAIN_YAW_LUT_SIZE-1][1];
  int lo = 0, hi = CHAIN_YAW_LUT_SIZE - 1;
  while (lo < hi - 1) {
    int mid = (lo + hi) / 2;
    if (CHAIN_YAW_LUT[mid][0] <= timeSec) lo = mid;
    else hi = mid;
  }
  float t0 = CHAIN_YAW_LUT[lo][0], t1 = CHAIN_YAW_LUT[hi][0];
  float y0 = CHAIN_YAW_LUT[lo][1], y1 = CHAIN_YAW_LUT[hi][1];
  float range = t1 - t0;
  if (range < 0.001f) return y0;
  float t = (timeSec - t0) / range;
  return y0 + (y1 - y0) * t;
}
